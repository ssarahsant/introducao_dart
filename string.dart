main() {
  // Declaração de uma variavel string + atribuição de valor
  String variavel1 = "Olá,";
  String variavel2 = " bom dia!!\n";

  // Concatenação simples de duas variaveis
  // *a concatenação de duas variaveis so pode ser fita entre tipagens iguais
  print(variavel1 + variavel2);

  // Tranformando o conteudo todo para letra maisucula
  print(variavel1 + variavel2.toUpperCase());
}
