main() {
  // Criação de um set de salas (conjunto)
  var salas = {'A101', 'A103', 'A104'};

  // Saída do Conjunto
  print(salas);

  // Inserção de dados no set
  salas.add('A102');

  // Saída do Conjunto
  print(salas);

  // Para localizar um elemento especifico dentro do conjunto
  print(salas.contains('A101'));
}
